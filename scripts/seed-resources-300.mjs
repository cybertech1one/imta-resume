async function insertBatch(batch, batchNum) {
	const values = [];
	const params = [];
	let paramIndex = 1;

	for (const r of batch) {
		const placeholders = [];
		for (const val of [
			r.id,
			r.name,
			r.name_fr,
			r.category,
			r.sub_category || null,
			r.description || null,
			r.description_fr || null,
			r.website || null,
			r.contact_email || null,
			r.contact_phone || null,
			r.location || null,
			r.fields || [],
			r.tags || [],
			r.is_free ?? true,
			r.rating || 4.0,
			true, // is_active
			r.sort_order,
		]) {
			placeholders.push(`$${paramIndex}`);
			params.push(val);
			paramIndex++;
		}
		values.push(`(${placeholders.join(", ")})`);
	}

	const sql = `
    INSERT INTO job_resource (
      id, name, name_fr, category, sub_category,
      description, description_fr, website, contact_email, contact_phone,
      location, fields, tags, is_free, rating, is_active, sort_order
    ) VALUES ${values.join(",\n")}
    ON CONFLICT (id) DO NOTHING
  `;

	const result = await client.query(sql, params);
	console.log(`  Batch ${batchNum}: ${result.rowCount} rows inserted (${batch.length} attempted)`);
	return result.rowCount;
}

