async function main() {
  console.log("================================================================");
  console.log("  IMTA Programs, Learning Resources & Success Stories Seeder");
  console.log("================================================================\n");
  console.log(`Database: ${DATABASE_URL.replace(/:[^@]*@/, ':***@')}`);

  await client.connect();
  console.log("Connected to PostgreSQL.\n");

  try {
    await seedImtaPrograms();
    await seedLearningResources();
    await seedSuccessStories();

    // Final summary
    console.log("\n================================================================");
    console.log("  FINAL ROW COUNTS");
    console.log("================================================================");
    const tables = ["imta_program", "learning_resource", "success_story"];
    for (const t of tables) {
      const count = await rowCount(t);
      console.log(`  ${t}: ${count} rows`);
    }
    console.log("\n  Done! All seed data inserted successfully.");
  } catch (err) {
    console.error("\n  ERROR:", err.message);
    if (err.detail) console.error("  Detail:", err.detail);
    if (err.hint) console.error("  Hint:", err.hint);
    process.exit(1);
  } finally {
    await client.end();
  }
}

