async function main() {
  console.log("=== Seed Regional Job Stats & Learning Resources ===\n");

  await client.connect();
  console.log("Connected to PostgreSQL\n");

  // Check existing counts
  const existingJobs = await client.query("SELECT count(*) as cnt FROM regional_job_stats");
  const existingResources = await client.query("SELECT count(*) as cnt FROM learning_resource");
  console.log(`Existing data:`);
  console.log(`  regional_job_stats: ${existingJobs.rows[0].cnt} rows`);
  console.log(`  learning_resource:  ${existingResources.rows[0].cnt} rows\n`);

  // Insert regional job stats
  console.log(`Inserting ${REGIONAL_JOB_STATS.length} regional job stats...`);
  const jobsResult = await insertRegionalJobStats(REGIONAL_JOB_STATS);
  console.log(`  Inserted: ${jobsResult.inserted}, Skipped: ${jobsResult.skipped}\n`);

  // Insert learning resources
  console.log(`Inserting ${LEARNING_RESOURCES.length} learning resources...`);
  const resourcesResult = await insertLearningResources(LEARNING_RESOURCES);
  console.log(`  Inserted: ${resourcesResult.inserted}, Skipped: ${resourcesResult.skipped}\n`);

  // Final counts
  const finalJobs = await client.query("SELECT count(*) as cnt FROM regional_job_stats");
  const finalResources = await client.query("SELECT count(*) as cnt FROM learning_resource");

  console.log("=== SUMMARY ===");
  console.log(`regional_job_stats: ${existingJobs.rows[0].cnt} -> ${finalJobs.rows[0].cnt} rows (+${finalJobs.rows[0].cnt - existingJobs.rows[0].cnt})`);
  console.log(`learning_resource:  ${existingResources.rows[0].cnt} -> ${finalResources.rows[0].cnt} rows (+${finalResources.rows[0].cnt - existingResources.rows[0].cnt})`);

  // Show region coverage
  const regions = await client.query("SELECT DISTINCT region FROM regional_job_stats ORDER BY region");
  console.log(`\nRegion coverage: ${regions.rows.length} distinct region entries`);

  // Show resource type breakdown
  const types = await client.query("SELECT resource_type, count(*) as cnt FROM learning_resource GROUP BY resource_type ORDER BY cnt DESC");
  console.log(`\nLearning resource types:`);
  types.rows.forEach((r) => console.log(`  ${r.resource_type}: ${r.cnt}`));

  // Show provider breakdown
  const providers = await client.query("SELECT provider, count(*) as cnt FROM learning_resource GROUP BY provider ORDER BY cnt DESC LIMIT 15");
  console.log(`\nTop providers:`);
  providers.rows.forEach((r) => console.log(`  ${r.provider}: ${r.cnt}`));

  await client.end();
  console.log("\nDone.");
}

main().catch((err) => {
  console.error("Fatal error:", err);
  client.end().catch(() => {});
  process.exit(1);
});
