// MAIN — Connect, Clear, Insert, Report
// ============================================================================

async function main() {
	const c = new Client("postgresql://postgres:postgres@localhost:5432/postgres");

	try {
		await c.connect();
		console.log("Connected to PostgreSQL.\n");

		// ── Step 1: Clear existing data ──────────────────────────────────
		console.log("1. Clearing existing data...");
		const delEmp = await c.query("DELETE FROM career_employer");
		const delIns = await c.query("DELETE FROM career_market_insight");
		console.log(`   Deleted ${delEmp.rowCount} employers, ${delIns.rowCount} insights.\n`);

		// ── Step 2: Insert employers in batches ──────────────────────────
		console.log(`2. Inserting ${EMPLOYERS.length} employers...`);
		let empInserted = 0;
		const BATCH_SIZE = 20;

		for (let i = 0; i < EMPLOYERS.length; i += BATCH_SIZE) {
			const batch = EMPLOYERS.slice(i, i + BATCH_SIZE);
			const values = [];
			const params = [];
			let paramIdx = 1;

			for (const e of batch) {
				const placeholders = [];
				for (let p = 0; p < 13; p++) {
					placeholders.push(`$${paramIdx++}`);
				}
				values.push(`(${placeholders.join(",")})`);

				params.push(
					genId(),                        // id
					e.name,                         // name
					e.sector,                       // sector
					e.sector_fr,                    // sector_fr
					e.location,                     // location
					e.location_fr || null,          // location_fr
					e.open_positions || 0,          // open_positions
					e.website || "#",               // website
					e.description || null,          // description
					e.description_fr || null,       // description_fr
					JSON.stringify(e.fields),       // fields (jsonb)
					true,                           // is_active
					i + batch.indexOf(e),           // sort_order
				);
			}

			const sql = `INSERT INTO career_employer (id, name, sector, sector_fr, location, location_fr, open_positions, website, description, description_fr, fields, is_active, sort_order) VALUES ${values.join(",")}`;
			await c.query(sql, params);
			empInserted += batch.length;
			process.stdout.write(`   Batch ${Math.floor(i / BATCH_SIZE) + 1}: ${empInserted}/${EMPLOYERS.length} inserted\r`);
		}
		console.log(`\n   Done: ${empInserted} employers inserted.\n`);

		// ── Step 3: Insert market insights in batches ────────────────────
		console.log(`3. Inserting ${MARKET_INSIGHTS.length} market insights...`);
		let insInserted = 0;

		for (let i = 0; i < MARKET_INSIGHTS.length; i += BATCH_SIZE) {
			const batch = MARKET_INSIGHTS.slice(i, i + BATCH_SIZE);
			const values = [];
			const params = [];
			let paramIdx = 1;

			for (const m of batch) {
				const placeholders = [];
				for (let p = 0; p < 11; p++) {
					placeholders.push(`$${paramIdx++}`);
				}
				values.push(`(${placeholders.join(",")})`);

				params.push(
					genId(),                        // id
					m.title,                        // title
					m.title_fr,                     // title_fr
					m.value,                        // value
					m.description || null,          // description
					m.description_fr || null,       // description_fr
					m.icon || null,                 // icon
					m.color || null,                // color
					m.field || null,                // field
					true,                           // is_active
					i + batch.indexOf(m),           // sort_order
				);
			}

			const sql = `INSERT INTO career_market_insight (id, title, title_fr, value, description, description_fr, icon, color, field, is_active, sort_order) VALUES ${values.join(",")}`;
			await c.query(sql, params);
			insInserted += batch.length;
			process.stdout.write(`   Batch ${Math.floor(i / BATCH_SIZE) + 1}: ${insInserted}/${MARKET_INSIGHTS.length} inserted\r`);
		}
		console.log(`\n   Done: ${insInserted} market insights inserted.\n`);

		// ── Step 4: Verify counts ────────────────────────────────────────
		console.log("4. Verifying...");
		const empCount = await c.query("SELECT count(*) FROM career_employer");
		const insCount = await c.query("SELECT count(*) FROM career_market_insight");
		const empActive = await c.query("SELECT count(*) FROM career_employer WHERE is_active = true");
		const insActive = await c.query("SELECT count(*) FROM career_market_insight WHERE is_active = true");

		console.log(`   career_employer:       ${empCount.rows[0].count} total (${empActive.rows[0].count} active)`);
		console.log(`   career_market_insight: ${insCount.rows[0].count} total (${insActive.rows[0].count} active)`);

		// Breakdown by field
		const empByField = await c.query("SELECT fields::text, count(*) FROM career_employer GROUP BY fields::text ORDER BY count DESC LIMIT 10");
		console.log("\n   Employers by field:");
		for (const row of empByField.rows) {
			console.log(`     ${row.fields}: ${row.count}`);
		}

		const insByField = await c.query("SELECT field, count(*) FROM career_market_insight GROUP BY field ORDER BY count DESC");
		console.log("\n   Insights by field:");
		for (const row of insByField.rows) {
			console.log(`     ${row.field || "(general)"}: ${row.count}`);
		}

		console.log("\n=== SEED COMPLETE ===");
		console.log(`Employers: ${empCount.rows[0].count} | Market Insights: ${insCount.rows[0].count}`);

	} catch (err) {
		console.error("\nERROR:", err.message);
		if (err.detail) console.error("DETAIL:", err.detail);
		process.exit(1);
	} finally {
		await c.end();
	}
}

main();
