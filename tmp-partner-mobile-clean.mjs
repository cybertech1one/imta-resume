import { chromium } from 'playwright';
const browser = await chromium.launch({ headless: true });
const page = await browser.newPage({ viewport: { width: 390, height: 844 }, deviceScaleFactor: 1 });
const errors = [];
page.on('console', (msg) => { if (msg.type() === 'error') errors.push(msg.text()); });
await page.goto('http://127.0.0.1:3041/partenaires', { waitUntil: 'domcontentloaded', timeout: 60000 });
await page.getByText('Recrutez des talents formes, prets pour le terrain.').first().waitFor({ state: 'visible', timeout: 30000 });
await page.waitForTimeout(1000);
const overflow = await page.evaluate(() => document.documentElement.scrollWidth - document.documentElement.clientWidth);
await page.screenshot({ path: process.env.PARTNER_MOBILE_CLEAN_SCREENSHOT, fullPage: false, animations: 'disabled' });
await browser.close();
console.log(JSON.stringify({ errors, overflow, screenshot: process.env.PARTNER_MOBILE_CLEAN_SCREENSHOT }, null, 2));
